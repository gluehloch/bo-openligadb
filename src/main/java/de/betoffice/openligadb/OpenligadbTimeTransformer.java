package de.betoffice.openligadb;

import java.text.ParseException;
import java.util.Locale;

import org.joda.time.DateTime;
import org.joda.time.format.DateTimeFormat;
import org.joda.time.format.DateTimeFormatter;
import org.springframework.format.datetime.joda.DateTimeParser;

/**
 * Transforms an openligadb date and time informations.
 * 
 * @author Andre Winkler
 */
public class OpenligadbTimeTransformer {

    public DateTime toDateTime(String dateTime) throws ParseException {
        DateTimeFormatter dtf = DateTimeFormat
                .forPattern("YYYY-MM-dd'T'HH:mm:ss");
        DateTimeParser dtp = new DateTimeParser(dtf);
        return dtp.parse(dateTime, Locale.GERMANY);
    }

}
